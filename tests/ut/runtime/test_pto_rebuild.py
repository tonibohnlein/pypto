def test_noop_when_pto_older_than_intermediate(tmp_path: Path, monkeypatch) -> None:
    work_dir = _setup_build_output(tmp_path, {"foo": [("aiv", "foo")]})
    pto = work_dir / "ptoas" / "foo.pto"
    out_cpp = work_dir / "ptoas" / "foo.cpp"
    out_cpp.write_text("// stale but newer mtime\n")
    # Make out_cpp clearly newer than pto.
    pto_mtime = pto.stat().st_mtime

    os.utime(out_cpp, (pto_mtime + 5, pto_mtime + 5))

    monkeypatch.setattr(pto_rebuild, "_ptoas_binary", lambda: "/fake/ptoas")
    monkeypatch.setattr(pto_rebuild, "_run_ptoas", lambda *_a, **_kw: pytest.fail("should not run"))

    touched = rebuild_kernel_cpp_from_pto(work_dir)
    assert touched == []


def test_rerun_when_pto_newer(tmp_path: Path, monkeypatch) -> None:
    work_dir = _setup_build_output(tmp_path, {"foo": [("aiv", "foo")]})
    pto = work_dir / "ptoas" / "foo.pto"
    out_cpp = work_dir / "ptoas" / "foo.cpp"
    out_cpp.write_text("// stale\n")
    # Pto is newer.
    later = time.time() + 10

    os.utime(pto, (later, later))

    monkeypatch.setattr(pto_rebuild, "_ptoas_binary", lambda: "/fake/ptoas")
    monkeypatch.setattr(pto_rebuild, "_run_ptoas", _fake_run_ptoas_for_funcs(["foo"]))

    touched = rebuild_kernel_cpp_from_pto(work_dir)
    assert touched == ["kernels/aiv/foo.cpp"]


def test_splices_single_function_target(tmp_path: Path, monkeypatch) -> None:
    work_dir = _setup_build_output(tmp_path, {"foo": [("aiv", "foo")]})
    later = time.time() + 10

    os.utime(work_dir / "ptoas" / "foo.pto", (later, later))

    monkeypatch.setattr(pto_rebuild, "_ptoas_binary", lambda: "/fake/ptoas")
    monkeypatch.setattr(pto_rebuild, "_run_ptoas", _fake_run_ptoas_for_funcs(["foo"], body_marker="FRESH"))

    rebuild_kernel_cpp_from_pto(work_dir)

    kernel_cpp = (work_dir / "kernels" / "aiv" / "foo.cpp").read_text()
    assert "// Kernel Function: foo" in kernel_cpp
    assert "#include <cstdint>" in kernel_cpp
    assert "kernel_entry" in kernel_cpp
    assert "OLD body for foo" not in kernel_cpp
    assert "FRESH body for foo" in kernel_cpp
    assert "static __aicore__ void foo()" in kernel_cpp
    assert PTOAS_BODY_BEGIN in kernel_cpp
    assert PTOAS_BODY_END in kernel_cpp


def test_splices_multiple_group_targets(tmp_path: Path, monkeypatch) -> None:
    work_dir = _setup_build_output(
        tmp_path,
        {"grp": [("aiv", "k1"), ("aic", "k2")]},
    )
    later = time.time() + 10

    os.utime(work_dir / "ptoas" / "grp.pto", (later, later))

    monkeypatch.setattr(pto_rebuild, "_ptoas_binary", lambda: "/fake/ptoas")
    monkeypatch.setattr(pto_rebuild, "_run_ptoas", _fake_run_ptoas_for_funcs(["k1", "k2"], body_marker="GRP"))

    touched = rebuild_kernel_cpp_from_pto(work_dir)
    assert set(touched) == {"kernels/aiv/k1.cpp", "kernels/aic/k2.cpp"}

    for core, func in [("aiv", "k1"), ("aic", "k2")]:
        cpp = (work_dir / "kernels" / core / f"{func}.cpp").read_text()
        assert f"GRP body for {func}" in cpp
        assert f"OLD body for {func}" not in cpp


