def test_per_pto_independence(tmp_path: Path, monkeypatch) -> None:
    """Only stale .pto entries trigger ptoas; fresh ones are left alone."""
    work_dir = _setup_build_output(
        tmp_path,
        {"stale": [("aiv", "stale")], "fresh": [("aiv", "fresh")]},
    )
    # stale.pto is newer than its intermediate cpp; fresh.pto is older.
    later = time.time() + 10
    earlier = time.time() - 10

    (work_dir / "ptoas" / "stale.cpp").write_text("// old\n")
    (work_dir / "ptoas" / "fresh.cpp").write_text("// up to date\n")
    os.utime(work_dir / "ptoas" / "stale.pto", (later, later))
    os.utime(work_dir / "ptoas" / "fresh.pto", (earlier, earlier))
    os.utime(work_dir / "ptoas" / "fresh.cpp", (later, later))

    called_for: list[str] = []

    def _spy(ptoas_bin: str, pto_path: Path, out_cpp: Path) -> None:
        called_for.append(pto_path.stem)
        _fake_run_ptoas_for_funcs([pto_path.stem])(ptoas_bin, pto_path, out_cpp)

    monkeypatch.setattr(pto_rebuild, "_ptoas_binary", lambda: "/fake/ptoas")
    monkeypatch.setattr(pto_rebuild, "_run_ptoas", _spy)

    touched = rebuild_kernel_cpp_from_pto(work_dir)
    assert called_for == ["stale"], "only the stale .pto should trigger ptoas"
    assert touched == ["kernels/aiv/stale.cpp"]


