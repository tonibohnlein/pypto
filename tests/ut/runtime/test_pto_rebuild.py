"""Unit tests for :mod:`pypto.runtime.debug.pto_rebuild`.

The real ``ptoas`` binary is never invoked: ``_run_ptoas`` is monkeypatched
to write a fake cpp, and ``_ptoas_binary`` is monkeypatched to a non-None
sentinel so the availability guard passes.
"""

from __future__ import annotations

import os
import time
from pathlib import Path

import pytest
from pypto.runtime.debug import pto_rebuild
from pypto.runtime.debug.pto_rebuild import (
    PTOAS_BODY_BEGIN,
    PTOAS_BODY_END,
    rebuild_kernel_cpp_from_pto,
)

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

_HEADER = """// Kernel Function: foo
// Generated by PyPTO IR Compiler (PTO backend)

#include <cstdint>
"""

_WRAPPER = """
extern "C" __aicore__ void kernel_entry(__gm__ int64_t* args)
{
    foo(args);
}
"""


def _wrapped_cpp(body: str, *, begin: str = PTOAS_BODY_BEGIN, end: str = PTOAS_BODY_END) -> str:
    """Construct a wrapper-shaped cpp with *body* between the sentinels."""
    return f"{_HEADER}\n{begin}\n{body}\n{end}\n{_WRAPPER}"


def _setup_build_output(
    tmp_path: Path,
    pto_units: dict[str, list[tuple[str, str]]],
) -> Path:
    """Create a minimal build_output skeleton.

    Args:
        pto_units: Mapping of ptoas unit name to a list of (core_type, func_name)
            pairs. Each (core, func) gets an existing ``kernels/<core>/<func>.cpp``
            file pre-populated with a wrapper-shaped layout (so splice succeeds).
    """
    ptoas_dir = tmp_path / "ptoas"
    ptoas_dir.mkdir(parents=True, exist_ok=True)
    for unit, targets in pto_units.items():
        (ptoas_dir / f"{unit}.pto").write_text(f"// stub pto for {unit}\n")
        for core, func in targets:
            cpp = tmp_path / "kernels" / core / f"{func}.cpp"
            cpp.parent.mkdir(parents=True, exist_ok=True)
            cpp.write_text(_wrapped_cpp(f"// OLD body for {func}\nstatic __aicore__ void {func}() {{}}\n"))
    return tmp_path


def _fake_run_ptoas_for_funcs(funcs: list[str], *, body_marker: str = "NEW"):
    """Return a fake `_run_ptoas` that writes a cpp defining *funcs*."""

    def _impl(_ptoas_bin: str, pto_path: Path, out_cpp: Path) -> None:
        lines = ["#include <pto/pto-inst.hpp>\n", "using namespace pto;\n"]
        for fn in funcs:
            lines.append(f"\nAICORE void {fn}() {{\n    // {body_marker} body for {fn}\n}}\n")
        out_cpp.write_text("".join(lines))

    return _impl


